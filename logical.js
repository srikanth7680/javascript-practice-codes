//Logical Operators
let x = 2,y = 3,z = 5
result = x<y && y <z //true AND operator(Both conditions should be true) &&--AND
result = x<y || y >z //true OR operator(Either One condition should be true) |--Or
n = !result          // If the result is true then the outcome is false !--NOT
console.log(result);//false
console.log(n);//true